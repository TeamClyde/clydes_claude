/**
 * Race an async work function against a deadline.
 * Resolves to a discriminated result — never rejects (a timeout is a value).
 * NON-PREEMPTIVE: the underlying work keeps running after a timeout
 * (regulation dossier §7). Keep any fan-out ≤ the concurrency cap so a
 * timed-out-but-still-running unit can't starve a slot.
 * @param {() => Promise<any>} workFn
 * @param {number} ms
 * @returns {Promise<{outcome:'done', value:any} | {outcome:'timeout'} | {outcome:'error', error:any}>}
 */
function withWatchdog(workFn, ms) {
  return new Promise((resolve) => {
    let settled = false;
    const finish = (r) => { if (!settled) { settled = true; clearTimeout(timer); resolve(r); } };
    const timer = setTimeout(() => { if (!settled) { settled = true; resolve({ outcome: 'timeout' }); } }, ms);
    Promise.resolve().then(workFn).then(
      (value) => finish({ outcome: 'done', value }),
      (error) => finish({ outcome: 'error', error }),
    );
  });
}

/**
 * Drive one work unit through the lifecycle FSM:
 *   PENDING → RUNNING → VALIDATING → { SUCCEEDED | RETRYING(repair) | TIMED_OUT | FAILED } → ABANDONED
 * A memo-hit (spec.store + spec.stepId present and cached) short-circuits to MEMOIZED → SUCCEEDED
 * without running work.
 * Validation failure is a CONTROL SIGNAL: the reason is fed back as repair
 * context and the work retried, reaching ABANDONED only after the retry budget.
 * runUnit ALWAYS resolves to a terminal state — it never rejects and never hangs
 * (the watchdog guarantees termination). This is what lets quorumBarrier be safe.
 * @param {object} spec
 * @param {(repair: string|null, ctx: {reason:string, value:any, attempt:number}|null) => Promise<any>} spec.work
 *   repair = validation reason string (backward-compat 1st arg); ctx = structured repair context
 *   (null on first attempt; ctx.attempt counts ALL loop iterations — crash + validation retries).
 * @param {(value:any) => ({ok:boolean, reason?:string} | Promise<{ok:boolean, reason?:string}>)} [spec.validate]
 *   default: non-null ⇒ ok. May be sync or async (the verdict is awaited).
 * @param {number} spec.timeoutMs
 * @param {number} [spec.maxRetries=1] crash/timeout retry budget
 * @param {number} [spec.maxValidationRetries=maxRetries] separate validation-repair retry budget
 * @param {{has(k):boolean, get(k):any, set(k,v):void}} [spec.store] opt-in step-result cache (e.g. a Map); intra-run only
 * @param {*} [spec.stepId] consumer-owned cache key — stable across resume, unique per distinct invocation, NEVER shared across quorum/best-of-N members. Memoization is OFF unless BOTH store and stepId are present.
 * @returns {Promise<{state:'SUCCEEDED', value:any, history:string[], memoized?:true} | {state:'ABANDONED', history:string[]}>}
 */
async function runUnit(spec) {
  const {
    work,
    validate = (v) => ({ ok: v != null }),
    timeoutMs,
    maxRetries = 1,
    maxValidationRetries = maxRetries, // default preserves prior single-budget behavior
    onEvent,
    store,
    stepId,
  } = spec;
  const history = [];
  const emit = (state, extra = {}) => { history.push(state); if (onEvent) onEvent({ state, ...extra }); };
  if (store && stepId != null && store.has(stepId)) {
    emit('MEMOIZED', { stepId, memoized: true });
    return { state: 'SUCCEEDED', value: store.get(stepId), history, memoized: true };
  }
  let crashRetriesLeft = maxRetries;
  let validationRetriesLeft = maxValidationRetries;
  let repair = null; // reason string (backward-compatible 1st arg to work)
  let ctx = null;    // structured { reason, value, attempt }
  let attempt = 0;   // counts ALL loop iterations (crash + validation retries), not validation retries alone
  while (true) {
    emit('RUNNING', { attempt });
    const res = await withWatchdog(() => work(repair, ctx), timeoutMs);
    if (res.outcome === 'timeout' || res.outcome === 'error') {
      emit(res.outcome === 'timeout' ? 'TIMED_OUT' : 'FAILED', { attempt });
      if (crashRetriesLeft > 0) { crashRetriesLeft--; attempt++; continue; }
      break;
    }
    emit('VALIDATING', { attempt });
    const verdict = await validate(res.value); // may be sync or async
    if (verdict.ok) {
      emit('SUCCEEDED', { attempt });
      if (store && stepId != null) store.set(stepId, res.value); // only SUCCEEDED writes — every terminal-fail path reaches ABANDONED below, never here
      return { state: 'SUCCEEDED', value: res.value, history };
    }
    if (validationRetriesLeft > 0) {
      validationRetriesLeft--; attempt++;
      repair = verdict.reason ?? 'validation failed';
      ctx = { reason: repair, value: res.value, attempt };
      emit('RETRYING', { attempt, reason: repair });
      continue;
    }
    break; // validation budget exhausted
  }
  emit('ABANDONED', {});
  return { state: 'ABANDONED', history };
}

/**
 * Run all units concurrently and proceed on a QUORUM of SUCCEEDED terminal
 * states — not on all units reaching SUCCEEDED. Because runUnit always reaches a
 * terminal state (SUCCEEDED or ABANDONED) within its watchdog budget, Promise.all
 * here can neither hang nor reject: a straggler is bounded to ABANDONED, so it can
 * never hold the barrier. SUCCEEDED values are captured, so abandoning is non-lossy.
 * @param {Array<object>} units  - runUnit specs
 * @param {number} threshold     - minimum SUCCEEDED count to consider the barrier healthy
 * @returns {Promise<{confirmed:any[], abandoned:number, degraded:boolean}>}
 */
async function quorumBarrier(units, threshold) {
  const results = await Promise.all(units.map((u) => runUnit(u)));
  const confirmed = results.filter((r) => r.state === 'SUCCEEDED').map((r) => r.value);
  const counts = {};
  for (const r of results) for (const s of r.history) counts[s] = (counts[s] ?? 0) + 1;
  return { confirmed, abandoned: results.length - confirmed.length, degraded: confirmed.length < threshold, counts };
}


// The fan-out front-door. Node module (imports fail-successfully.mjs); Workflow scripts consume it via the INLINED engine bundle (scripts/build-engine-bundle.mjs) — the sandbox has no module system.
// Wraps the fail-successfully engine; enforces DispatchPolicy (maxInFlight batching +
// post-hoc reactive token gate). NON-PREEMPTIVE: gates new spawns; in-flight units finish.
// See docs/explanation/orchestration-regulation-layer.md (§9 — the regulation-layer build spec).

const DEFAULT_POLICY = {
  maxInFlight: 8,                 // keep ≤ runtime cap min(16, cores−2) so nothing queues
  perUnitTimeoutMs: undefined,    // required per call (watchdog → fast detect-and-abandon)
  maxRetries: 1,                  // crash/timeout retries
  maxValidationRetries: 1,        // validation-repair retries (separate budget)
  quorum: undefined,              // default computed: ceil(units.length / 2)
  modelTier: null,                // pin Haiku/Sonnet; threaded into the consumer's agent({model})
  tokenBudget: null,              // max output tokens for this fan-out (null = no limit)
  estimatedTokensPerUnit: 0,      // coarse fallback projection only; if 0 AND no getRemainingBudget, the projection gate is inert — use getRemainingBudget for real gating
  budgetReserve: 0.9,             // stop at 90% to bound non-preemptable in-flight overshoot
  getRemainingBudget: null,       // () => live remaining tokens (Workflow: () => budget.remaining())
  onOverloadBackoff: 'exponential', // passthrough convention; the consumer's work() honors it on 529/API-overload — the lib does not act on it
};

function chunk(arr, size) {
  const out = [];
  for (let i = 0; i < arr.length; i += size) out.push(arr.slice(i, i + size));
  return out;
}

function withDefaults(u, p) {
  return { timeoutMs: p.perUnitTimeoutMs, maxRetries: p.maxRetries, maxValidationRetries: p.maxValidationRetries, ...u };
}

/**
 * Batch `units` (chunks of maxInFlight) through quorumBarrier; a post-hoc reactive token gate
 * runs between batches. Returns { confirmed, abandoned, degraded, counts, stoppedReason }.
 * NOTE: read `degraded` together with `stoppedReason` — a 'token-budget' early-stop can leave
 * confirmed < quorum (degraded=true) even when every unit that actually RAN succeeded.
 */
async function parallelFanout(units, policy = {}) {
  const p = { ...DEFAULT_POLICY, ...policy };
  if (p.perUnitTimeoutMs == null) throw new TypeError('perUnitTimeoutMs is required in DispatchPolicy');
  const quorum = p.quorum ?? Math.ceil(units.length / 2);
  const confirmed = [];
  const counts = {};
  let abandoned = 0;
  let launched = 0;
  let stoppedReason;
  for (const batch of chunk(units, Math.max(1, p.maxInFlight))) {
    if (p.tokenBudget != null) {
      const remaining = p.getRemainingBudget ? p.getRemainingBudget()
                                             : p.tokenBudget - launched * p.estimatedTokensPerUnit;
      if (remaining * p.budgetReserve < batch.length * p.estimatedTokensPerUnit) { stoppedReason = 'token-budget'; break; }
    }
    // per-batch `degraded` is ignored; the final `degraded` is recomputed over the whole fan-out below
    const r = await quorumBarrier(batch.map((u) => withDefaults(u, p)), quorum);
    confirmed.push(...r.confirmed);
    abandoned += r.abandoned;
    for (const [s, n] of Object.entries(r.counts)) counts[s] = (counts[s] ?? 0) + n;
    launched += batch.length;
  }
  return { confirmed, abandoned, degraded: confirmed.length < quorum, counts, stoppedReason };
}

/**
 * Run steps in order; each step's SUCCEEDED value feeds the next step's work(prior, repair, ctx).
 * Validation-as-feedback applies per step. Halts on the first ABANDONED.
 */
async function sequentialChain(steps, policy = {}) {
  const p = { ...DEFAULT_POLICY, ...policy };
  if (p.perUnitTimeoutMs == null) throw new TypeError('perUnitTimeoutMs is required in DispatchPolicy');
  const results = [];
  let prev;
  let completed = 0;
  let stoppedReason;
  for (const step of steps) {
    const prior = prev;
    // bridge: runUnit calls work(repair, ctx); the consumer's step.work takes (prior, repair, ctx)
    const spec = withDefaults({ ...step, work: (repair, ctx) => step.work(prior, repair, ctx) }, p);
    const r = await runUnit(spec);
    results.push(r);
    if (r.state !== 'SUCCEEDED') { stoppedReason = 'abandoned'; break; }
    prev = r.value;
    completed++;
  }
  return { results, completed, stoppedReason };
}

/**
 * Monolith→fan-out reviewer path: fan out review lenses, then ONE batched verify over all
 * findings (NOT 3-votes-per-finding — see feedback_workflow_model_pinning). policy.verify
 * is an optional async (findings) => findings.
 */
async function dimensionalReview(dimensions, policy = {}) {
  const p = { ...DEFAULT_POLICY, ...policy };
  if (p.perUnitTimeoutMs == null) throw new TypeError('perUnitTimeoutMs is required in DispatchPolicy');
  const review = await parallelFanout(dimensions, p);
  let findings = review.confirmed.flat();
  let verifyDegraded = false;
  if (p.verify) {
    // ONE batched verify (runs as a single runUnit → maxRetries+1 total calls on failure, never per-finding).
    // Contract: p.verify(findings) resolves to the filtered findings array.
    const v = await runUnit(withDefaults({ work: () => p.verify(findings) }, p));
    if (v.state === 'SUCCEEDED') findings = v.value;
    else verifyDegraded = true; // verify abandoned → findings are the UNVERIFIED pre-verify list; caller must check this flag
  }
  return { findings, counts: review.counts, degraded: review.degraded, verifyDegraded };
}

