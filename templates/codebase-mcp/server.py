# Tool 1 — codebase_search_symbol
# ---------------------------------------------------------------------------


@mcp.tool()
def codebase_search_symbol(name: str, fuzzy: bool = False) -> list[dict]:
    """Search the symbols index for a symbol by name.

    Args:
        name:  Exact symbol name to search for (case-sensitive when fuzzy=False).
        fuzzy: When True, perform a case-insensitive partial-match search across
               all symbol names.  Useful when you know part of a name or are
               unsure of capitalisation.

    Returns:
        A list of matching symbol records, each containing:
            id         — stable node ID (e.g. "src/utils/auth.py::get_token")
            type       — node type: file | class | function | method | env_var | route
            name       — symbol name
            file       — relative file path from repo root
            line_start — line number where the symbol is defined
    """
    results: list[dict] = []

    if not fuzzy:
        # Exact match — check the symbols index first, then fall back to node scan
        if name in _symbols_index:
            entry = _symbols_index[name]
            # The symbols index may store a single node or a list
            entries = entry if isinstance(entry, list) else [entry]
            for e in entries:
                results.append(
                    {
                        "id": e.get("id", ""),
                        "type": e.get("type", ""),
                        "name": e.get("name", name),
                        "file": e.get("file", ""),
                        "line_start": e.get("line_start"),
                    }
                )
        else:
            # Fall back to scanning nodes list
            for node in _nodes_by_id.values():
                if node.get("name") == name:
                    results.append(
                        {
                            "id": node["id"],
                            "type": node.get("type", ""),
                            "name": node.get("name", ""),
                            "file": node.get("file", ""),
                            "line_start": node.get("line_start"),
                        }
                    )
    else:
        # Fuzzy — case-insensitive substring match over all nodes
        needle = name.lower()
        for node in _nodes_by_id.values():
            if needle in node.get("name", "").lower():
                results.append(
                    {
                        "id": node["id"],
                        "type": node.get("type", ""),
                        "name": node.get("name", ""),
                        "file": node.get("file", ""),
                        "line_start": node.get("line_start"),
                    }
                )

    return results


