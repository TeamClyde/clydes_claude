# Tool 4 — codebase_get_env_var
# ---------------------------------------------------------------------------


@mcp.tool()
def codebase_get_env_var(var_name: str) -> dict:
    """Look up an environment variable in the graph.

    The lookup is case-insensitive so "dynamodb_table_name" finds
    "DYNAMODB_TABLE_NAME".

    Args:
        var_name: Environment variable name to look up.

    Returns:
        A dict containing:
            var_name    — canonical variable name
            defined_in  — file where the variable is declared (e.g. serverless.yml)
            default     — default value if defined; None otherwise
            read_by     — list of node IDs that read this variable
        Returns an empty dict if the variable is not found.
    """
    key = var_name.lower()
    entry = _env_vars_index.get(key)

    if entry is None:
        # Try scanning env_var nodes directly
        for node in _nodes_by_id.values():
            if node.get("type") == "env_var" and node.get("name", "").lower() == key:
                # Build read_by from edges
                read_by = [
                    e.get("from", "")
                    for e in _edges
                    if e.get("type") == "reads_env" and e.get("to") == node["id"]
                ]
                return {
                    "var_name": node.get("name", var_name),
                    "defined_in": node.get("file", ""),
                    "default": node.get("env_var_default"),
                    "read_by": read_by,
                }
        return {}

    if isinstance(entry, dict):
        return {
            "var_name": entry.get("var_name", var_name),
            "defined_in": entry.get("defined_in", entry.get("file", "")),
            "default": entry.get("default", entry.get("env_var_default")),
            "read_by": entry.get("read_by", []),
        }

    return {}


