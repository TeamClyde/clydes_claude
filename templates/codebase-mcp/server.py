@mcp.tool()
def codebase_get_entry_points() -> list[dict]:
    """Return all entry-point nodes in the codebase.

    Entry points are nodes with is_entry_point == True — Lambda handlers,
    HTTP route handlers, queue consumers, scheduled jobs, etc.

    Returns:
        A list of entry-point records, each containing:
            id         — node ID
            name       — symbol name
            file       — relative file path
            line_start — line number of the entry point
            trigger    — trigger description (e.g. "sqs:SendNotificationQueue",
                         "http:GET /users", "eventbridge:myapp.order.placed")
                         May be None if not captured during indexing.
    """
    return [
        {
            "id": node["id"],
            "name": node.get("name", ""),
            "file": node.get("file", ""),
            "line_start": node.get("line_start"),
            "trigger": node.get("trigger"),
        }
        for node in _nodes_by_id.values()
        if node.get("is_entry_point") is True
    ]


