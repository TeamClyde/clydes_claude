# Tool 6 — codebase_search_api_endpoints
# ---------------------------------------------------------------------------


@mcp.tool()
def codebase_search_api_endpoints(path_filter: str = "") -> list[dict]:
    """Search for API endpoints captured in the graph.

    Queries the endpoints index, which captures both exposed HTTP routes (type
    "route") and the handler nodes that back them.

    Args:
        path_filter: Optional substring to filter results by URL path.
                     E.g. "/users" returns all endpoints whose path contains
                     "/users".  Pass "" to return all endpoints.

    Returns:
        A list of endpoint records, each containing:
            method   — HTTP method (GET, POST, etc.) or None for non-HTTP triggers
            path     — URL path or trigger description
            handler  — node ID of the handling function/method
            file     — file where the handler lives
    """
    results: list[dict] = []

    # Flatten the endpoints index — its structure may vary by graph builder version.
    # Support both list-of-dicts and dict-of-dicts.
    raw_endpoints: list[dict] = []
    if isinstance(_endpoints_index, list):
        raw_endpoints = _endpoints_index
    elif isinstance(_endpoints_index, dict):
        for v in _endpoints_index.values():
            if isinstance(v, list):
                raw_endpoints.extend(v)
            elif isinstance(v, dict):
                raw_endpoints.append(v)

    for ep in raw_endpoints:
        path = ep.get("path", "")
        if path_filter and path_filter.lower() not in path.lower():
            continue
        results.append(
            {
                "method": ep.get("method"),
                "path": path,
                "handler": ep.get("handler", ep.get("handler_id", "")),
                "file": ep.get("file", ""),
            }
        )

    # Also include route nodes from the graph that aren't in the endpoints index
    seen_handlers = {r["handler"] for r in results}
    for node in _nodes_by_id.values():
        if node.get("type") != "route":
            continue
        if node["id"] in seen_handlers:
            continue
        path = node.get("route_prefix", node.get("name", ""))
        if path_filter and path_filter.lower() not in path.lower():
            continue
        results.append(
            {
                "method": None,
                "path": path,
                "handler": node["id"],
                "file": node.get("file", ""),
            }
        )

    return results


