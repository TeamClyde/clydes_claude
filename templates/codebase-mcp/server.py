# Tool 3 — codebase_find_dependencies
# ---------------------------------------------------------------------------


@mcp.tool()
def codebase_find_dependencies(file_path: str) -> dict:
    """Find what a file imports and what imports it.

    Scans edges with type "imports" that involve the given file path.

    Args:
        file_path: Relative file path from the repo root.
                   Example: "src/services/notification.py"

    Returns:
        A dict with two keys:
            imports     — list of {name, from} — symbols/modules this file imports
            imported_by — list of file paths that import this file
    """
    imports: list[dict] = []
    imported_by: list[str] = []

    for edge in _edges:
        if edge.get("type") != "imports":
            continue

        edge_from: str = edge.get("from", "")
        edge_to: str = edge.get("to", "")

        # Normalise: edge endpoints may be node IDs ("file.py::Symbol") or bare paths
        from_file = edge_from.split("::")[0] if "::" in edge_from else edge_from
        to_file = edge_to.split("::")[0] if "::" in edge_to else edge_to

        if from_file == file_path:
            # This file imports something
            to_node = _nodes_by_id.get(edge_to, {})
            imports.append(
                {
                    "name": to_node.get("name", edge_to),
                    "from": to_file,
                }
            )
        elif to_file == file_path:
            # Something imports this file
            if from_file not in imported_by:
                imported_by.append(from_file)

    return {"imports": imports, "imported_by": imported_by}


