@mcp.tool()
def codebase_find_callers(symbol_id: str) -> list[dict]:
    """Find all call-sites that invoke the given symbol.

    Uses the pre-built callers index; falls back to scanning edges when the
    symbol is not in the index.

    Args:
        symbol_id: The node ID of the symbol to look up callers for.
                   Example: "src/utils/cognito.py::get_cognito_user"

    Returns:
        A list of caller records, each containing:
            caller_id    — node ID of the calling symbol
            caller_name  — human-readable name of the caller
            caller_file  — file in which the call appears
            call_line    — line number of the call expression
    """
    if symbol_id in _callers_index:
        raw = _callers_index[symbol_id]
        callers = raw if isinstance(raw, list) else [raw]
        return [
            {
                "caller_id": c.get("caller_id", c.get("id", "")),
                "caller_name": c.get("caller_name", c.get("name", "")),
                "caller_file": c.get("caller_file", c.get("file", "")),
                "call_line": c.get("call_line", c.get("line")),
            }
            for c in callers
        ]

    # Fallback: scan edges for calls that point to symbol_id
    results: list[dict] = []
    for edge in _edges:
        if edge.get("type") == "calls" and edge.get("to") == symbol_id:
            caller_node = _nodes_by_id.get(edge.get("from", ""), {})
            results.append(
                {
                    "caller_id": edge.get("from", ""),
                    "caller_name": caller_node.get("name", ""),
                    "caller_file": edge.get("file", caller_node.get("file", "")),
                    "call_line": edge.get("line"),
                }
            )
    return results


